namespace AIStudio.Tools.Rust;

/// <summary>
/// Identifies a global keyboard shortcut.
/// </summary>
public enum Shortcut
{
    /// <summary>
    /// Null pattern - no shortcut assigned or unknown shortcut.
    /// </summary>
    NONE = 0,
    
    /// <summary>
    /// Toggles voice recording on/off.
    /// </summary>
    VOICE_RECORDING_TOGGLE,
}
